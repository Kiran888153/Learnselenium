public class Learnstaticmethod {
	
	static int a = 7;
	
	public void display() {
		
		System.out.println("display here");	

	}
	
	

	
	
	static {
		
		System.out.println("static block");
	}

	public static void main(String[] args) {
		
		System.out.println(a);
		
		
	

	}
}


package practicejava;

public class Calculator {
	
	public int add(int num1, int num2, int num3) {
		int result = num1+num2+num3;
		return result;
		}
	
	public int sub(int num1,int num2) {
		int result = num1-num2;
		return result;
		}
	
	public int mul(int num1, int num2) {
		int result = num1*num2;
		return result;
	}
	
	
	
	

}
